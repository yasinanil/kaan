package topics;

public class C08_IfStatement {

    public static void main(String[] args) {

        int number = 6;

        if (number<10){
            System.out.println(number+ " is less than 10.");
        }

        if (number%2==0){
            System.out.println(number+ " is even.");
        }

        if (number%2!=0){
            System.out.println(number+ " is odd.");
        }


    }

}

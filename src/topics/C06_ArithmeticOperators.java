package topics;

public class C06_ArithmeticOperators {

    public static void main(String[] args) {

        int x = 5;
        int y = 2;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);
        //System.out.println(x / 0);//ArithmeticException: / by zero
        System.out.println(10.0/3);


    }

}
